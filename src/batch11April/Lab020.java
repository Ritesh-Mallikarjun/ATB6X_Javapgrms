package batch11April;

public class Lab020 {
    public static void main(String[] args) {
        System.out.println("this is the program to check even and odd numbers from 1 to 10");
        for (int i = 0; i <= 10; i++) {
            if (i % 2 == 0) {
                System.out.println("hi " + i + " this is even number from 1 to 10 ");
            }
            else {
                System.out.println("hi " + i + " this is odd number from 1 to 10 ");
            }

        }
    }
}
